function vertexto() {
    var mostrarcondiciones=document.getElementById("condiciones");
    var ocultarbotonver=document.getElementById("ver")
    mostrarcondiciones.style.display="block";
    ocultarbotonver.style.display="none";

}
function ocultartexto() {
    var ocultartexto=document.getElementById("condiciones");
    var mostrarboton=document.getElementById("ver")
    ocultartexto.style.display="none";
    mostrarboton.style.display="block";
}
